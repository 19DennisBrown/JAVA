

public class Switch {
    public static void main( String [] args ){

        int position = 3;

        switch (position){
            case 1:
                System.out.println(" This is the best possible.");
            break;
            case 2:
                System.out.println(" This is the second-best position possible.");
            break;
            case 3:
                System.out.println(" This is the third-best position possible.");
            break;
            default:
                System.out.println(" Position is not ranked. ");
        }

        System.out.println("End of code");
    }
}


